char AAW_GRID_OVERLAY(char a, char b)
{
    if(a == ' ' || a == '\0')           //whitespace + b transforms to b
        return b;
    else if(b == ' ' || b == '\0')      //a + whitespace transforms to a
        return a;
    else if(a == b)                     //nothing
        return a;
    else                                //'+' +  x  or '|' + '-' transforms to '+'
        return '+';
}

